import numpy as np
import pandas as pd
from pathlib import Path
from typing import Union, Tuple
import json

class QuestionnaireAnalysis:
    """
    Reads and analyzes data generated by the questionnaire experiment.
    Should be able to accept strings and pathlib.Path objects.
    """

    def __init__(self, data_fname: Union[Path, str]):
        if type(data_fname) == str:
            self.data_fname = Path.cwd().joinpath(data_fname)
        else:
            self.data_fname = data_fname

    def read_data(self):
        """Reads the json data located in self.data_fname into memory, to
        the attribute self.data.
        """
        list_of_dict = json.loads(self.data_fname.read_text())
        self.data = pd.DataFrame(list_of_dict).set_index("id")

    def show_age_distrib(self) -> Tuple[np.ndarray, np.ndarray]:
        """Calculates and plots the age distribution of the participants.

        Returns
        -------
        hist : np.ndarray
        Number of people in a given bin
        bins : np.ndarray
        Bin edges
        """    
        ages = self.data["age"].replace("nan", 0).to_numpy()
        bins = np.asarray([(x,x+10) for x in range(0,100,10)])
        count = []

        for values in bins:
            count.append(np.count_nonzero((ages>values[0])&(ages<values[1])))

        hist = np.ndarray(count)

        return hist, bins



